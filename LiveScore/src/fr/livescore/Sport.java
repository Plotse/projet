package fr.livescore;

public class Sport {
	
	// Attributs de la classe 
	private int id;
	private String nom;
	
	// Getters
	public int getId() {
		return id;
	}
	
	public String getNom() {
		return nom;
	}
	
	// Setters
	public void setId(int i) {
		id = i;
	}
	
	public void setNom(String n) {
		nom = n;
	}
}
